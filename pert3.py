# we will learn type data 

x = 11 # bilangan bulat (integer "int")
print("nilai dari x :", x)
print(type(x))

y = 2.5 # bilangan pecahan (float "float")
print("nilai dari y :", y)
print(type(y))

nama = "arya" # tipe data string (tulisan "str")
print("nilai dari nama :", nama)
print(type(nama))

kebenaran = False # tipe data benar salah (True / False "boll")
print("nilai dari kebenaran :", kebenaran)
print(type(kebenaran))