# operasi aritmatika
# X / + - 

# operasi penjumlahan +
a = 9
b = 9
hasil = a + b
print(a, "+", b, "=", hasil)

# pengurangan -
a = 23
b =12
hasil = a - b
print(a, "-", b, "=", hasil)

# pembagian /
a = 9
b = 12
hasil = a / b
print(a, ":", b, "=", hasil)

# perkalian *
a = 9
b = 12
hasil = a * b
print(a, "x", b, "=", hasil)

# perpangkatan menggunakan ** (eksponesial
a = 9
b = 12
hasil = a ** b
print(a, "pangkat", b, "=", hasil)

# sisa pembagian (modulus) %
a = 12
b = 8
hasil = a % b
print(a, "mod", b, "=", hasil)

# floor division
a = 15
b = 4
hasil = a // b
print(a, "floor division", b, "=", hasil)


